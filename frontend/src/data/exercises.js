export const getRandomWorkout = (level, bmi, targetAreas) => {
  const workout = {
    name: `Custom ${level.charAt(0).toUpperCase() + level.slice(1)} Workout`,
    exercises: [],
    duration: ''
  };

  // Add exercises for each selected body part
  targetAreas.forEach(area => {
    const availableExercises = exercises[level][area];
    if (availableExercises) {
      // Get 1-2 random exercises for each selected area
      const numExercises = Math.floor(Math.random() * 2) + 1;
      const shuffled = [...availableExercises].sort(() => 0.5 - Math.random());
      const selectedExercises = shuffled.slice(0, numExercises).map(exercise => ({
        ...exercise,
        name: exercise.name,
        sets: exercise.sets,
        reps: exercise.reps,
        duration: exercise.duration,
        description: exercise.description,
        benefits: exercise.benefits,
        perSide: exercise.perSide
      }));
      workout.exercises.push(...selectedExercises);
    }
  });

  // Adjust workout based on BMI if needed
  if (bmi >= 30) {
    workout.exercises = workout.exercises.map(exercise => ({
      ...exercise,
      sets: Math.max(1, exercise.sets - 1),
      reps: exercise.reps ? Math.floor(exercise.reps * 0.8) : undefined,
      duration: exercise.duration ? 
        exercise.duration.includes('seconds') ? 
          `${Math.floor(parseInt(exercise.duration) * 0.8)} seconds` :
          `${Math.floor(parseInt(exercise.duration) * 0.8)} minutes` : 
        undefined
    }));
  }

  // Calculate estimated duration
  const totalExercises = workout.exercises.length;
  const avgTimePerExercise = 3; // minutes
  workout.duration = `${totalExercises * avgTimePerExercise} minutes`;

  return workout;
};